    private boolean camposVacios() {
        if (inputCodigoProducto.getText().isEmpty() || inputNombreProducto.getText().isEmpty() || inputOrigenProducto.getText().isEmpty() || inputPrecioProducto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Los campos no pueden estar vacíos");
            return true;
        }
        return false;
    }


