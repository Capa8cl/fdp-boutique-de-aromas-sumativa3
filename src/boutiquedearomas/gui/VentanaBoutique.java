    private void btnAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProductoActionPerformed

        inputDatos();

        if (camposVacios()) {
            return;
        }

        try {
            int precioProductoInt = Integer.parseInt(prec);

            if (precioProductoInt < 0) {
                JOptionPane.showMessageDialog(this, "El precio debe ser mayor o igual a 0 y debe ser un número entero.");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El precio debe ser un número entero.");
            return;
        }

        control.agregarProducto(cod, nom, prec, orig);
        control.mostrarProductos(mostrarResultados);
        JOptionPane.showMessageDialog(this, "Producto agregado correctamente.");
        limpiarInputs();

    }//GEN-LAST:event_btnAgregarProductoActionPerformed

    private void btnEditarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarProductoActionPerformed

        if (control.listaVacia()) {
            JOptionPane.showMessageDialog(this, "No hay productos registrados para editar. Ingrese un producto antes de editar.");
            mostrarResultados.setText("--- Lista de Productos ---\nNo existen productos registrados actualmente.");
            return;
        }

        inputDatos();

        if (camposVacios()) {
            return;
        }

        if (!control.existeProducto(cod)) {
            JOptionPane.showMessageDialog(this, "El código ingresado no existe.");
            return;
        }

        int confirmarEditar = JOptionPane.showConfirmDialog(null, "¿Está seguro que desea editar el producto?", "Confirmar", JOptionPane.YES_NO_OPTION);

        if (confirmarEditar == JOptionPane.YES_OPTION) {

            boolean correcto = control.editarProducto(cod, nom, prec, orig);

            if (correcto) {
                control.mostrarProductos(mostrarResultados);
                JOptionPane.showMessageDialog(this, "Producto (s) modificado correctamente.");
                limpiarInputs();
            } else {
                JOptionPane.showMessageDialog(this, "El precio debe ser un número válido igual o mayor a 0.");
            }
        }

    }//GEN-LAST:event_btnEditarProductoActionPerformed

    private void btnEstadisticasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEstadisticasActionPerformed
        mostrarResultados.setText("");

        if (!control.listaVacia()) {
            control.mostrarProductos(mostrarResultados);
            control.mostrarEstadisticas(mostrarResultados);
            mostrarResultados.append("\n" + control.mensajeTotalGastado());
        } else {
            mostrarResultados.setText("--- Estadísticas ---\nNo existen productos registrados para mostrar estadísticas.");
        }

    }//GEN-LAST:event_btnEstadisticasActionPerformed

    private void btnBorrarUltimoRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarUltimoRegistroActionPerformed
        if (!control.listaVacia()) {
            int confirmarEditar = JOptionPane.showConfirmDialog(null, "¿Está seguro que desea eliminar el último producto?", "Confirmar", JOptionPane.YES_NO_OPTION);

            if (confirmarEditar == JOptionPane.YES_OPTION) {
                control.borrarUltimo();
                mostrarResultados.setText("");

                if (control.listaVacia()) {
                    mostrarResultados.setText("--- Lista de Productos ---\nNo existen productos registrados actualmente.");
                } else {
                    control.mostrarProductos(mostrarResultados);
                }

                JOptionPane.showMessageDialog(this, "Último producto eliminado correctamente.");
                limpiarInputs();
            }
        } else {
            JOptionPane.showMessageDialog(this, "No existen productos registrados, no se borró ningún registro.");
            mostrarResultados.setText("--- Lista de Productos ---\nNo existen productos registrados actualmente.");
        }

    }//GEN-LAST:event_btnBorrarUltimoRegistroActionPerformed

